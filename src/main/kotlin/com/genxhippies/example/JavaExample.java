package com.genxhippies.example;

import com.genxhippies.korean.KoUtils;

public class JavaExample {
    void printAction(String name) {
        System.out.println(KoUtils.은는(name) + " 즐겁습니다.");
        System.out.println(KoUtils.을를(name) + " 좋아합니다.");
        System.out.println(KoUtils.이가(name) + " 일어났습니다.");
        System.out.println(KoUtils.과와(name) + " 함께 밥을 먹었습니다.");
        System.out.println(KoUtils.으로(name) + " 가고 있습니다.");
        System.out.println(KoUtils.이여(name) + "! 창문을 열어다오.");
        System.out.println(KoUtils.이가_가(name) + " 좋습니다.");
    }
}
