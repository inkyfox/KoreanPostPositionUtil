package com.genxhippies.example

import com.genxhippies.korean.*

class KotlinExample {
    fun printAction(name: String) {
        println("${name.은} 즐겁습니다.")
        println("${name.을} 좋아합니다.")
        println("${name.이} 일어났습니다.")
        println("${name.과} 함께 밥을 먹었습니다.")
        println("${name.으로} 가고 있습니다.")
        println("${name.이여}! 창문을 열어다오.")

        println("${name.이_가} 좋습니다.")
    }
}